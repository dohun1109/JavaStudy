package JavaProject.Chart02;

public class IncrementOp1 {
    public static void main(String[] args) {
        int x = 5;      //7
        int y = 10;     //9
        int z = x+++ ++x + y--;//5 , 7, 9



        System.out.println("x : "+ x);
        System.out.println("y : "+ y);
        System.out.println("z : "+ z);
    }



}
